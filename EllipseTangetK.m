function [pt,line]=EllipseTangetK(Elli,k)
%本函数用于已知斜率，求两条切线
%椭圆采用一般式
f=zeros(6,1);
f(1)=(Elli(5)^2-4*Elli(3));
f(3)=(Elli(2)^2-4*Elli(1)*Elli(3));
f(2)=(4*Elli(3)*Elli(4)-2*Elli(2)*Elli(5));
f(4)=(Elli(4)*Elli(5)*2-4*Elli(2));
f(5)=(Elli(4)*Elli(2)*2-4*Elli(1)*Elli(5));
f(6)=(Elli(4)^2-4*Elli(1));
b=zeros(2,1);
b(1)=-(f(5)+k*f(2))+sqrt((f(5)+k*f(2))^2-4*f(3)*(f(4)*k+f(1)*k^2+f(6)));
b(1)=b(1)/2/f(3);
b(2)=-(f(5)+k*f(2))-sqrt((f(5)+k*f(2))^2-4*f(3)*(f(4)*k+f(1)*k^2+f(6)));
b(2)=b(2)/2/f(3);
line=zeros(2,2);
line(1,1)=k;
line(2,1)=k;
line(:,2)=b;
pt=zeros(2,2);
A=(Elli(1)+Elli(2)*k+Elli(3)*k^2);
B=(Elli(2)*b(1)+2*Elli(3)*k*b(1)+Elli(5)*k+Elli(4));
pt(1,1)=-B/2/A;
B=(Elli(2)*b(2)+2*Elli(3)*k*b(2)+Elli(5)*k+Elli(4));
pt(2,1)=-B/2/A;
pt(1,2)=line(1,1)*pt(1,1)+line(1,2);
pt(2,2)=line(2,1)*pt(2,1)+line(2,2);
end